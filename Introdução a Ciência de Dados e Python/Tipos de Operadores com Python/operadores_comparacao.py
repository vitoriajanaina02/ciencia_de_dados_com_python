# OPERADORES DE COMPARAÇÃO

# O que são:
# São opradores utilizados para comparar dois valores.

# Igualdade
saldo = 450
saque = 200

print(saldo == saque) # false

# Diferença
saldo = 450
saque = 200

print(saldo != saque) # true

# Maior que / maior ou igual
saldo = 450
saque = 200

print(saldo > saque) # true
print(saldo >= saque) # true

# Menor que / maior ou igual
saldo = 450
saque = 200

print(saldo < saque) # false
print(saldo <= saque) # false