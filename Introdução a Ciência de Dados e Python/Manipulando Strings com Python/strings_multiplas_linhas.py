# STRING MÚLTIPLAS LINHAS
#
# Strings de múltplas linhas são definidas informando 3 aspas simples ou duplas
# durante a atribuição. Elas podem ocupar várias linhas do código, e todos os
# espaços em branco são incluídos na string final.
#
# String Triplas
nome = "Vitoria"

mensagem = f"""
Olá, meu nome é {nome}. 
Eu estou aprendendo Python.
"""

print(mensagem)